 /**
  * create an IO form for the WbfsysProfile entity
  *
  * @param Entity $entity the entity object
  * @param array $fields list with all elements that shoul be shown in the ui
  * @namedParam TFlag $params named parameters
  * {
  *   string prefix     : prefix for the inputs;
  *   string target     : target for;
  *   boolean readOnly  : set all elements to readonly;
  *   boolean refresh   : refresh the elements in an ajax request ;
  *   boolean sendElement : if true, then the system will send the elements in
  *   ajax requests als serialized html and not only just as value
  * }
  */
  public function renderForm( $params = null  )
  {

    $params  = $this->checkNamedParams( $params );
    $i18n     = $this->view->i18n;
    
    if( $params->access )
      $this->access = $params->access;

    // add the entity to the view
    $this->view->addVar( 'entity', $this->entity );
    $this->view->addVar( 'entityWbfsysProfile', $this->entity );


    $this->db     = $this->getDb();
    
    if( !$this->suffix )
    {
      if( 'create' != $params->context )
        $this->suffix = $this->rowid?:null;
    }

    if( $this->target )
      $sendTo = 'wgt-input-'.$this->target.'-tostring';
    else
      $sendTo = 'wgt-input-wbfsys_profile'.($this->suffix?'-'.$this->suffix:'').'-tostring';

    $this->customize();

    $inputToString = $this->view->newInput( 'input'.$this->prefix.'ToString' , 'Text' );
    $inputToString->addAttributes
    (
      array
      (
        'name'  => 'wbfsys_profile[id_wbfsys_profile-tostring]',
        'id'    => $sendTo,
        'value' => $this->entity->text(),
      )
    );

    $inputToString->setReadOnly( $this->readOnly );
    $inputToString->refresh = $this->refresh;

    $this->input_WbfsysProfile_MParent( $params );
    $this->input_WbfsysProfile_Name( $params );
    $this->input_WbfsysProfile_AccessKey( $params );
    $this->input_WbfsysProfile_FlagCore( $params );
    $this->input_WbfsysProfile_Description( $params );
    $this->input_WbfsysProfile_Revision( $params );
    $this->input_WbfsysProfile_Rowid( $params );
    $this->input_WbfsysProfile_MTimeCreated( $params );
    $this->input_WbfsysProfile_MRoleCreate( $params );
    $this->input_WbfsysProfile_MTimeChanged( $params );
    $this->input_WbfsysProfile_MRoleChange( $params );
    $this->input_WbfsysProfile_MVersion( $params );
    $this->input_WbfsysProfile_MUuid( $params );


  }//end public function renderForm */

 /**
  * create the ui element for field m_parent
  * @param TFlag $params named parameters
  * @return void
  */
  public function input_WbfsysProfile_MParent( $params )
  {
    $i18n     = $this->view->i18n;

    if( !Webfrap::classLoadable( 'WbfsysProfile_Entity' ) )
    {
      if(DEBUG)
        Debug::console( 'Entity WbfsysProfile not exists' );

      Log::warn( 'Looks like the Entity: WbfsysProfile is missing' );

      return;
    }


      //p: Window
      $objidWbfsysProfile = $this->entity->getData( 'm_parent' ) ;

      // entity ids can never be 0 so thats ok
      if
      (
        !$objidWbfsysProfile
          || !$entityWbfsysProfile = $this->db->orm->get
          (
            'WbfsysProfile',
            $objidWbfsysProfile
          )
      )
      {
        $entityWbfsysProfile = $this->db->orm->newEntity( 'WbfsysProfile' );
      }

      $inputMParent = $this->view->newInput( 'inputWbfsysProfileMParent', 'Window' );
      $this->items['wbfsys_profile-m_parent'] = $inputMParent;
      $inputMParent->addAttributes(array
      (
        'readonly'  => 'readonly',
        'name'      => 'wbfsys_profile[m_parent]',
        'id'        => 'wgt-input-wbfsys_profile_m_parent'.($this->suffix?'-'.$this->suffix:''),
          'class'     => 'wcm wcm_ui_tip medium'.($this->assignedForm?' asgd-'.$this->assignedForm:''),
        'title'     => $i18n->l( 'Insert value for {@attr@} ({@src@})', 'wbf.label', array( 'attr' => 'Parent Node', 'src' => 'Profile' ) ),
      ));

      if( $this->assignedForm )
        $inputMParent->assignedForm = $this->assignedForm;

      $inputMParent->setWidth( 'medium' );

      $inputMParent->setData( $this->entity->getData( 'm_parent' )  );
      $inputMParent->setReadonly( $this->fieldReadOnly( 'wbfsys_profile', 'm_parent' ) );
      $inputMParent->setRequired( $this->fieldRequired( 'wbfsys_profile', 'm_parent' ) );
      $inputMParent->setLabel( $i18n->l( 'Parent Node', 'wbfsys.profile.label' ) );


      $listUrl = 'modal.php?c=Wbfsys.Profile.selection'
        .'&amp;suffix='.$this->suffix.'&amp;input=wbfsys_profile_m_parent'.($this->suffix?'-'.$this->suffix:'');

      $inputMParent->setListUrl ( $listUrl );
      $inputMParent->setListIcon( 'control/connect.png' );
      $inputMParent->setEntityUrl( 'maintab.php?c=Wbfsys.Profile.edit' );
      $inputMParent->conEntity         = $entityWbfsysProfile;
      $inputMParent->refresh           = $this->refresh;
      $inputMParent->serializeElement  = $this->sendElement;



      $inputMParent->view = $this->view;
      $inputMParent->buildJavascript( 'wgt-input-wbfsys_profile_m_parent'.($this->suffix?'-'.$this->suffix:'') );
      $this->view->addJsCode( $inputMParent );

      // activate the category
      $this->view->addVar
      (
        'showCat'.$this->namespace.'_Default' ,
        true
      );

  }//end public function input_WbfsysProfile_MParent */

